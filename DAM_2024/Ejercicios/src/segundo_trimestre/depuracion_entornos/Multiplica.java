/*
Modificar el programa anterior, incluyendo un bucle que muestre la tabla de multiplicar del 5 del 1 al 10
(bucle for con el contador de la variable i).
 */
public class Multiplica {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println("5 X " + i + " = " + (5 * i));
        }
    }
}
