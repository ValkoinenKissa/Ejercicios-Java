package estructuras_control_flujo;
/*
 * Imprime 3 veces 5 asteriscos empleando dos for anidados
 */

public class CreBucleAnidadoAsteriscos {
	public static void main(String[] args) {
		for(int i=0; i<3; i++) {
			System.out.println();
			for(int j=0; j<5; j++) {
				System.out.print("*");
				
			}
		}
		
	}

}
