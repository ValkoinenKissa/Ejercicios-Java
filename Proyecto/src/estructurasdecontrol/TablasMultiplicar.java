package estructurasdecontrol;
//import java.util.Scanner;
public class TablasMultiplicar {

	public static void main(String[] args) 
	{
		int result = 0;
		for (int mult = 1; mult<=10; mult ++)
			
		for (int i = 0; i <= 10; i++ ) {
			result = mult * i;
			System.out.println("\n" + mult + "*" + i + "=" + result);

		}

	}

}
