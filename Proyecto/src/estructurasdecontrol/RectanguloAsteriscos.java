package estructurasdecontrol;

public class RectanguloAsteriscos {

	public static void main(String[] args) 
	{
		for(int j = 1; j<=3; j++)
		{
		//Hacemos un bucle for que imprima 5 asteriscos
		for(int i = 1; i<=5; i++) 
			System.out.print("*");
			//Saltamos a la siguiente linea
			System.out.println();
		}
		
	}

}
