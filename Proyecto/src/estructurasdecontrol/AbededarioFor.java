package estructurasdecontrol;

public class AbededarioFor {

	public static void main(String[] args) 
{
	for (char letra = 'a'; letra <= 'z'; letra++) {
		
		System.out.print(letra + " ");
	}
		
	System.out.println("\n\n");	
	
	for (int letra = 'a'; letra <= 'z'; letra++) {
		
		System.out.print(letra + " ");
	}
		
}

}
